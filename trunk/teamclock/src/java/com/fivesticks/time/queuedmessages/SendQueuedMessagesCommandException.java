package com.fivesticks.time.queuedmessages;

/**
 * @author REID
 */
public class SendQueuedMessagesCommandException extends Exception {

    /**
     *  
     */
    public SendQueuedMessagesCommandException() {
        super();

    }

    /**
     * @param arg0
     */
    public SendQueuedMessagesCommandException(String arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     */
    public SendQueuedMessagesCommandException(Throwable arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     * @param arg1
     */
    public SendQueuedMessagesCommandException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}