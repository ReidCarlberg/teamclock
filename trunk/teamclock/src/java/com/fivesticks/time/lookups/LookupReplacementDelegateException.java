package com.fivesticks.time.lookups;

/**
 * @author Reid
 */
public class LookupReplacementDelegateException extends Exception {

    /**
     * 
     */
    public LookupReplacementDelegateException() {
        super();

    }

    /**
     * @param message
     */
    public LookupReplacementDelegateException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public LookupReplacementDelegateException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public LookupReplacementDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}
