package com.fivesticks.time.common;

/**
 * @author Reid
 *
 */
public class AbstractServiceDelegateException extends Exception {

    /**
     * 
     */
    public AbstractServiceDelegateException() {
        super();

    }

    /**
     * @param arg0
     */
    public AbstractServiceDelegateException(String arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     */
    public AbstractServiceDelegateException(Throwable arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     * @param arg1
     */
    public AbstractServiceDelegateException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}
