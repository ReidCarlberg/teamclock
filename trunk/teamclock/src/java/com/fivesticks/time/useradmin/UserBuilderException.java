package com.fivesticks.time.useradmin;

/**
 * @author Reid
 */
public class UserBuilderException extends Exception {

    /**
     *  
     */
    public UserBuilderException() {
        super();

    }

    /**
     * @param message
     */
    public UserBuilderException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public UserBuilderException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public UserBuilderException(String message, Throwable cause) {
        super(message, cause);

    }

}