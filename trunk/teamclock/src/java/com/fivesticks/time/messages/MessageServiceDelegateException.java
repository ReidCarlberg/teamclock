package com.fivesticks.time.messages;

public class MessageServiceDelegateException extends Exception {

    public MessageServiceDelegateException() {
        super();

    }

    public MessageServiceDelegateException(String arg0) {
        super(arg0);

    }

    public MessageServiceDelegateException(Throwable arg0) {
        super(arg0);

    }

    public MessageServiceDelegateException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}
