package com.fivesticks.time.system;

/**
 * @author Reid
 *  
 */
public class ContentUpdateException extends Exception {

    /**
     *  
     */
    public ContentUpdateException() {
        super();
    }

    /**
     * @param arg0
     */
    public ContentUpdateException(String arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     */
    public ContentUpdateException(Throwable arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     * @param arg1
     */
    public ContentUpdateException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

}