package com.fivesticks.time.systemowner;

/**
 * @author Reid
 */
public class SystemOwnerServiceDelegateException extends Exception {

    /**
     *  
     */
    public SystemOwnerServiceDelegateException() {
        super();

    }

    /**
     * @param message
     */
    public SystemOwnerServiceDelegateException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public SystemOwnerServiceDelegateException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public SystemOwnerServiceDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}