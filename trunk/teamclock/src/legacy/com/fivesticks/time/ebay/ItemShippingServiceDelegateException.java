package com.fivesticks.time.ebay;

/**
 * @author Reid
 */
public class ItemShippingServiceDelegateException extends Exception {

    /**
     * 
     */
    public ItemShippingServiceDelegateException() {
        super();

    }

    /**
     * @param message
     */
    public ItemShippingServiceDelegateException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public ItemShippingServiceDelegateException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public ItemShippingServiceDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}
