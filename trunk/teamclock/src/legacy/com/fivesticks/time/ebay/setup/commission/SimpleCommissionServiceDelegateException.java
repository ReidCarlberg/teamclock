package com.fivesticks.time.ebay.setup.commission;

/**
 * @author Reid
 *
 */
public class SimpleCommissionServiceDelegateException extends Exception {

    /**
     * 
     */
    public SimpleCommissionServiceDelegateException() {
        super();

    }

    /**
     * @param arg0
     */
    public SimpleCommissionServiceDelegateException(String arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     */
    public SimpleCommissionServiceDelegateException(Throwable arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     * @param arg1
     */
    public SimpleCommissionServiceDelegateException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}
