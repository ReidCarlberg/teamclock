package com.fivesticks.time.todo.queue;

/**
 * @author REID
 */
public class EmailServiceProviderException extends Exception {

    /**
     * 
     */
    public EmailServiceProviderException() {
        super();

    }

    /**
     * @param arg0
     */
    public EmailServiceProviderException(String arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     */
    public EmailServiceProviderException(Throwable arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     * @param arg1
     */
    public EmailServiceProviderException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}
