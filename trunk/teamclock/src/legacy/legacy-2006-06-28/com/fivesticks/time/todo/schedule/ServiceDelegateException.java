public class ServiceDelegateException extends Exception {

    public ServiceDelegateException() {
        super();

    }

    public ServiceDelegateException(String message) {
        super(message);

    }

    public ServiceDelegateException(Throwable cause) {
        super(cause);

    }

    public ServiceDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}