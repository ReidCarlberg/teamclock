package com.fivesticks.util.images.legacy;

/**
 * @author Reid
 */
public class ScalePictureException extends Exception {

    /**
     * 
     */
    public ScalePictureException() {
        super();

    }

    /**
     * @param message
     */
    public ScalePictureException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public ScalePictureException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public ScalePictureException(String message, Throwable cause) {
        super(message, cause);

    }

}
