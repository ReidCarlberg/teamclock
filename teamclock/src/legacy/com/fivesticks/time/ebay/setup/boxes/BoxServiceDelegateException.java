package com.fivesticks.time.ebay.setup.boxes;

/**
 * @author Reid
 */
public class BoxServiceDelegateException extends Exception {

    /**
     * 
     */
    public BoxServiceDelegateException() {
        super();

    }

    /**
     * @param message
     */
    public BoxServiceDelegateException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public BoxServiceDelegateException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public BoxServiceDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}
