/**
 * @author Reid
 *
 */
public class EbayFormServiceDelegateException extends Exception {

    /**
     * 
     */
    public EbayFormServiceDelegateException() {
        super();

    }

    /**
     * @param arg0
     */
    public EbayFormServiceDelegateException(String arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     */
    public EbayFormServiceDelegateException(Throwable arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     * @param arg1
     */
    public EbayFormServiceDelegateException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}
