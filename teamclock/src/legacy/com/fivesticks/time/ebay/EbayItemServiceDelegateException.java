package com.fivesticks.time.ebay;

/**
 * @author REID
 */
public class EbayItemServiceDelegateException extends Exception {

    /**
     * 
     */
    public EbayItemServiceDelegateException() {
        super();

    }

    /**
     * @param arg0
     */
    public EbayItemServiceDelegateException(String arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     */
    public EbayItemServiceDelegateException(Throwable arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     * @param arg1
     */
    public EbayItemServiceDelegateException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}
