package com.fivesticks.time.useradmin;

/**
 * @author Reid
 */
public class UserTypeDeterminatorException extends Exception {

    /**
     *  
     */
    public UserTypeDeterminatorException() {
        super();

    }

    /**
     * @param message
     */
    public UserTypeDeterminatorException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public UserTypeDeterminatorException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public UserTypeDeterminatorException(String message, Throwable cause) {
        super(message, cause);

    }

}