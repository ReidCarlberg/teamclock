package com.fivesticks.time.timeclock.util;

/**
 * @author Reid
 */
public class PayPeriodDeterminatorException extends Exception {

    /**
     *  
     */
    public PayPeriodDeterminatorException() {
        super();

    }

    /**
     * @param message
     */
    public PayPeriodDeterminatorException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public PayPeriodDeterminatorException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public PayPeriodDeterminatorException(String message, Throwable cause) {
        super(message, cause);

    }

}