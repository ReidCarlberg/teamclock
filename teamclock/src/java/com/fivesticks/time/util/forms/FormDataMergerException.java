package com.fivesticks.time.util.forms;

/**
 * @author Reid
 */
public class FormDataMergerException extends Exception {

    /**
     * 
     */
    public FormDataMergerException() {
        super();

    }

    /**
     * @param message
     */
    public FormDataMergerException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public FormDataMergerException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public FormDataMergerException(String message, Throwable cause) {
        super(message, cause);

    }

}
