package com.fivesticks.time.employee;

/**
 * @author Reid
 *
 */
public class EmployeeServiceDelegateException extends Exception {

    /**
     * 
     */
    public EmployeeServiceDelegateException() {
        super();

    }

    /**
     * @param arg0
     */
    public EmployeeServiceDelegateException(String arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     */
    public EmployeeServiceDelegateException(Throwable arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     * @param arg1
     */
    public EmployeeServiceDelegateException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}
