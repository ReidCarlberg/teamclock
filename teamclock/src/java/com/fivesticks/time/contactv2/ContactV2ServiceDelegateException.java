package com.fivesticks.time.contactv2;

/**
 * @author Reid
 */
public class ContactV2ServiceDelegateException extends Exception {

    /**
     * 
     */
    public ContactV2ServiceDelegateException() {
        super();

    }

    /**
     * @param message
     */
    public ContactV2ServiceDelegateException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public ContactV2ServiceDelegateException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public ContactV2ServiceDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}
