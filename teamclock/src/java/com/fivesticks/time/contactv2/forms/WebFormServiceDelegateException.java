package com.fivesticks.time.contactv2.forms;

/**
 * @author Reid
 */
public class WebFormServiceDelegateException extends Exception {

    /**
     * 
     */
    public WebFormServiceDelegateException() {
        super();

    }

    /**
     * @param message
     */
    public WebFormServiceDelegateException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public WebFormServiceDelegateException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public WebFormServiceDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}
