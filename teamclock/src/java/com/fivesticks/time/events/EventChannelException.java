package com.fivesticks.time.events;

/**
 * @author REID
 */
public class EventChannelException extends Exception {

    /**
     * 
     */
    public EventChannelException() {
        super();

    }

    /**
     * @param arg0
     */
    public EventChannelException(String arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     */
    public EventChannelException(Throwable arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     * @param arg1
     */
    public EventChannelException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}
