package com.fivesticks.time.object.metrics;

/**
 * @author Reid
 */
public class ObjectMetricServiceDelegateException extends Exception {

    /**
     *  
     */
    public ObjectMetricServiceDelegateException() {
        super();

    }

    /**
     * @param message
     */
    public ObjectMetricServiceDelegateException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public ObjectMetricServiceDelegateException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public ObjectMetricServiceDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}