package com.fivesticks.time.object.metrics;

/**
 * @author REID
 */
public class ObjectMetricNotFoundException extends Exception {

    /**
     *  
     */
    public ObjectMetricNotFoundException() {
        super();

    }

    /**
     * @param arg0
     */
    public ObjectMetricNotFoundException(String arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     */
    public ObjectMetricNotFoundException(Throwable arg0) {
        super(arg0);

    }

    /**
     * @param arg0
     * @param arg1
     */
    public ObjectMetricNotFoundException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

}