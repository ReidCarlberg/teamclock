package com.fivesticks.time.object.comments;

/**
 * @author Reid
 */
public class ObjectCommentServiceDelegateException extends Exception {

    /**
     *  
     */
    public ObjectCommentServiceDelegateException() {
        super();

    }

    /**
     * @param message
     */
    public ObjectCommentServiceDelegateException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public ObjectCommentServiceDelegateException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public ObjectCommentServiceDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}