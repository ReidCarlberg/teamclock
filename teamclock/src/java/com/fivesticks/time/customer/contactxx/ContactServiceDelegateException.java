package com.fivesticks.time.customer.contactxx;

/**
 * @author Reid
 */
public class ContactServiceDelegateException extends Exception {

    /**
     * 
     */
    public ContactServiceDelegateException() {
        super();

    }

    /**
     * @param message
     */
    public ContactServiceDelegateException(String message) {
        super(message);

    }

    /**
     * @param cause
     */
    public ContactServiceDelegateException(Throwable cause) {
        super(cause);

    }

    /**
     * @param message
     * @param cause
     */
    public ContactServiceDelegateException(String message, Throwable cause) {
        super(message, cause);

    }

}
